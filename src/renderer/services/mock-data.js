function generateAnalyticsData() {
    const data = [];
    const startDate = new Date('2023-07-01');
    const endDate = new Date('2024-11-22');
    const dayMs = 24 * 60 * 60 * 1000;

    for (let d = new Date(startDate); d <= endDate; d = new Date(d.getTime() + dayMs)) {
        const dayIndex = Math.floor((d - startDate) / dayMs);
        const trend = 1 + dayIndex * 0.005;
        const weekday = d.getDay();
        const weekendDip = (weekday === 0 || weekday === 6) ? 0.6 : 1;
        const noise = 0.8 + Math.random() * 0.4;

        const clicks = Math.round(50 * trend * weekendDip * noise);
        const impressions = Math.round(clicks * (6 + Math.random() * 4));
        const position = Math.max(1, 15 - dayIndex * 0.015 + (Math.random() - 0.5) * 3);

        const year = d.getFullYear();
        const month = String(d.getMonth() + 1).padStart(2, '0');
        const day = String(d.getDate()).padStart(2, '0');

        data.push({
            date: `${year}-${month}-${day}`,
            clicks,
            impressions,
            position: Math.round(position * 10) / 10,
        });
    }

    return data;
}


